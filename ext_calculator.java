/**
 * The `ExtendedCalculator` class in Java provides methods to calculate the square and cube of a given
 * float number.
 */
class ExtendedCalculator{
    
    /**
     * The function "square" takes a float input and returns the square of that input.
     * 
     * @param operand_1 The `operand_1` parameter is a floating-point number that represents the value
     * for which you want to calculate the square.
     * @return The function `square` is returning the square of the `operand_1` value.
     */
    float square(float operand_1){
        return(operand_1 * operand_1);
    } 

    /**
     * The `cube` function in Java calculates the cube of a given float number.
     * 
     * @param operand_1 The `operand_1` parameter is a floating-point number that represents the value
     * for which you want to calculate the cube.
     * @return The function `cube` returns the cube of the `operand_1` value passed to it.
     */
    float cube(float operand_1){
        return(operand_1 * operand_1 * operand_1);
    }

}